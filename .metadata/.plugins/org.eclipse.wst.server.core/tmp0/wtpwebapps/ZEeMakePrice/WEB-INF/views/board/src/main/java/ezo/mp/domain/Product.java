package ezo.mp.domain;

public class Product {

	private String productCode;
	private String productName;
	private int productCount;
	private int productPrice;
	private String productPic;
	private String productPic2;

	public String getProductPic2() {
		return productPic2;
	}

	public void setProductPic2(String productPic2) {
		this.productPic2 = productPic2;
	}

	public String getProductPic() {
		return productPic;
	}

	public void setProductPic(String productPic) {
		this.productPic = productPic;
	}

	@Override
	public String toString() {
		return "Product [productCode=" + productCode + ", productName="
				+ productName + ", productCount=" + productCount
				+ ", productPrice=" + productPrice + "]";
	}

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public int getProductCount() {
		return productCount;
	}

	public void setProductCount(int productCount) {
		this.productCount = productCount;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

}
